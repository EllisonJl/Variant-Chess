package uk.ac.standrews.variantchessgame.model;

/**
 * Represents the color of a chess piece in the game.
 * Chess pieces can be either WHITE or BLACK.
 */
public enum Color {
    /**
     * Represents the color WHITE for chess pieces.
     * Typically, the player with WHITE pieces moves first.
     */
    WHITE,

    /**
     * Represents the color BLACK for chess pieces.
     * Typically, the player with BLACK pieces moves second.
     */
    BLACK;

}
